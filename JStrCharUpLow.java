public class JStrCharUpLow {
    public static void main(String[] args) {
        String s1 = "Good Morning";
        System.out.println("Actual String :" + s1+"\nTo Upper Case: "+s1.toUpperCase()+"\nTo Lower Case: "+s1.toLowerCase());
    }
}

// **************OUTPUT**************

/*

Actual String :Good Morning
To Upper Case: GOOD MORNING
To Lower Case: good morning

*/